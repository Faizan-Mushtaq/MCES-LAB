SRCH EQU 11H
	RESULT EQU 70H
	N EQU 5
	ORG 0000H
		SJMP AHEAD
		DATA1: DB 24H,45H,72H,30H,11H
		AHEAD:
			MOV DPTR,#DATA1
			MOV R3,#N
		CONT:
			CLR A
			MOVC A,@A+DPTR
			CJNE A,#SRCH,NOTFND
			MOV RESULT,#0FFH
			SJMP DONE
		NOTFND:
			INC DPTR
			DJNZ R3,CONT
			MOV RESULT,#0FH
		DONE:SJMP $
			END
