COUNT EQU 05H
MIN EQU 71H
	ORG 0000H
	JMP AHEAD
		
	ORG 0100H
		ELE: DB 0FH,10H,06H,0A1H,0FFH
	
	AHEAD: 
		MOV DPTR,#ELE
		CLR A
		MOVC A,@A+DPTR
		MOV MIN,A
		MOV R0,#COUNT
		DEC R0
	BACK:
		INC DPTR
		CLR A
		MOVC A,@A+DPTR
		MOV R1,A
		SUBB A,MIN   ;if a>min no carry
		JNC SKIP    ;JC FOR MAX
		MOV MIN,R1
	SKIP:
		DJNZ R0,BACK
		SJMP $
		END
		